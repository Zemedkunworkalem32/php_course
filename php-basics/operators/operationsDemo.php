<?php
// PHP Arithmetic Operators Demo

$x = 10;
$y = 3;

echo "x = $x, y = $y\n\n";

// Addition
echo "Addition: x + y = " . ($x + $y) . "\n"; // 13

// Subtraction
echo "Subtraction: x - y = " . ($x - $y) . "\n"; // 7

// Multiplication
echo "Multiplication: x * y = " . ($x * $y) . "\n"; // 30

// Division
echo "Division: x / y = " . ($x / $y) . "\n"; // 3.3333

// Modulus
echo "Modulus: x % y = " . ($x % $y) . "\n"; // 1

// Exponentiation
echo "Exponentiation: x ** y = " . ($x ** $y) . "\n"; // 1000
?>